export function generateFallbackAnalysis(decision: 'BUY' | 'SELL' | 'HOLD', price: number, confidence: number): string {
  const atoms = MASTER_ATOMS[decision];
  
  const getRand = (arr: string[]) => arr[Math.floor(Math.random() * arr.length)];
  
  const opening = getRand(atoms.openings).replace('PRICE', price.toLocaleString());
  const state = getRand(atoms.marketStates);
  const pulse = getRand(atoms.liquidityPulse).replace('CONF', confidence.toString());
  const sentiment = getRand(atoms.aiSentiment).replace('PRICE', price.toLocaleString());
  const conclusion = getRand(atoms.conclusions);

  return `${opening}، ${state}، ${pulse}. ${sentiment} ${conclusion}`;
}

